#include<stdio.h>

void main() {
	
	if(4 <= 5)
		printf("4 is lesser than 5\n");		//simple statement
	
	//as soon as {} will be used, with if, it will become compound statement.
	if(4 <= 5) {
		
		printf("4 is lesser than 5\n");		//compound statement
		printf("4 is lesser than 6\n");		//compound statement
	}
}
