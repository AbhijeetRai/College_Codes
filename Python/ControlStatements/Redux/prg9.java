class Demo {

	public static void main(String[] args) {
		
		/*
		 * boolean required in if condition	
		 * if(1)
		 *	System.out.println("In first if");
		 */

		/**
		 * Only boolean is needed in 'condition'
		 * if("Hi")
		 *	System.out.println("In second if");
		 */
			
		/**
		 * only 'boolean' for conditions in 'Java'
		 * if('A')
		 *	System.out.println("Inside third if");
		 */

		if(true)
			System.out.println("Inside fourth if");
		
		//comparison returns boolean
		System.out.println(2 > 1);

		if(2 > 1)
			System.out.println("2 is greater than 1");
	}	
}
