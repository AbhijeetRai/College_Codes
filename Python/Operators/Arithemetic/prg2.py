x = 4
y = 1.5

ans = x + y
print(ans,type(ans))

ans = x - y
print(ans,type(ans))

ans = x * y
print(ans,type(ans))

ans = x / y
print(ans,type(ans))

ans = x % y
print(ans,type(ans))

#floor divison, performs divison and give only integer quotient.
ans = x // y
print(ans,type(ans))

#exponential, this acts like x raise to y.
ans = x ** y
print(ans,type(ans))